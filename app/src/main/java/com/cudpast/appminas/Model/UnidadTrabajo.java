package com.cudpast.appminas.Model;

public class UnidadTrabajo {

    private String id;
    private String nameUT;
    private String aliasUT;
    private String addressUT;
    private String placeUT;

    public UnidadTrabajo() {
    }

    public String getAliasUT() {
        return aliasUT;
    }

    public void setAliasUT(String aliasUT) {
        this.aliasUT = aliasUT;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNameUT() {
        return nameUT;
    }

    public void setNameUT(String nameUT) {
        this.nameUT = nameUT;
    }

    public String getAddressUT() {
        return addressUT;
    }

    public void setAddressUT(String addressUT) {
        this.addressUT = addressUT;
    }

    public String getPlaceUT() {
        return placeUT;
    }

    public void setPlaceUT(String placeUT) {
        this.placeUT = placeUT;
    }
}
